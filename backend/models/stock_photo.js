const mongoose = require('mongoose');

const Schema = mongoose.Schema;

const stockPhotoSchema = new Schema({
  title: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  imageUrl: {
    type: String,
    required: true
  },
  userId: {
    type: Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  tags: []
});

module.exports = mongoose.model('StockPhoto', stockPhotoSchema);